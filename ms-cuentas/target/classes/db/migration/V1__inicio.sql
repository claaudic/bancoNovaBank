CREATE TABLE tipo_cuenta
(
    id_tipo_cuenta     BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    nombre_tipo_cuenta VARCHAR(30) NOT NULL,
    CONSTRAINT pk_tipo_cuenta PRIMARY KEY (id_tipo_cuenta),
    CONSTRAINT uc_tipo_cuenta_nombre UNIQUE (nombre_tipo_cuenta)
);

CREATE TABLE cuenta
(
    id_cuenta       BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    numero_cuenta   VARCHAR(20)    NOT NULL,
    fecha_creacion  DATE           NOT NULL,
    saldo           NUMERIC(19, 2) NOT NULL,
    rut_cliente     VARCHAR(12)    NOT NULL,
    estado          VARCHAR(15)    NOT NULL,
    id_tipo_cuenta  BIGINT         NOT NULL,
    CONSTRAINT pk_cuenta PRIMARY KEY (id_cuenta),
    CONSTRAINT uc_cuenta_numero UNIQUE (numero_cuenta),
    CONSTRAINT fk_cuenta_tipo FOREIGN KEY (id_tipo_cuenta) REFERENCES tipo_cuenta (id_tipo_cuenta),
    CONSTRAINT ck_cuenta_saldo_no_negativo CHECK (saldo >= 0),
    CONSTRAINT ck_cuenta_estado CHECK (estado IN ('ACTIVA', 'INACTIVA', 'BLOQUEADA'))
);

CREATE INDEX idx_cuenta_rut_cliente ON cuenta (rut_cliente);
CREATE INDEX idx_cuenta_estado ON cuenta (estado);
CREATE INDEX idx_cuenta_fecha_creacion ON cuenta (fecha_creacion);
CREATE INDEX idx_cuenta_tipo ON cuenta (id_tipo_cuenta);

CREATE TABLE tarjeta
(
    id_tarjeta        BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    numero_tarjeta    VARCHAR(19) NOT NULL,
    fecha_vencimiento DATE        NOT NULL,
    cvv               VARCHAR(4)  NOT NULL,
    estado            VARCHAR(15) NOT NULL,
    id_cuenta         BIGINT      NOT NULL,
    CONSTRAINT pk_tarjeta PRIMARY KEY (id_tarjeta),
    CONSTRAINT uc_tarjeta_numero UNIQUE (numero_tarjeta),
    CONSTRAINT fk_tarjeta_cuenta FOREIGN KEY (id_cuenta) REFERENCES cuenta (id_cuenta),
    CONSTRAINT ck_tarjeta_estado CHECK (estado IN ('ACTIVA', 'INACTIVA', 'BLOQUEADA', 'VENCIDA'))
);

CREATE INDEX idx_tarjeta_cuenta ON tarjeta (id_cuenta);
CREATE INDEX idx_tarjeta_estado ON tarjeta (estado);
CREATE INDEX idx_tarjeta_fecha_vencimiento ON tarjeta (fecha_vencimiento);

