ALTER TABLE cliente
    ADD CONSTRAINT FK_CLIENTE_ON_ID_PROFESION FOREIGN KEY (id_profesion) REFERENCES profesion (id_profesion);

ALTER TABLE direccion_cliente
    ADD CONSTRAINT FK_DIRECCIONCLIENTE_ON_RUT_CLIENTE FOREIGN KEY (rut_cliente) REFERENCES cliente (rut_cliente);

