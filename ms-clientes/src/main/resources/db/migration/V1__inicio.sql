CREATE TABLE cliente
(
    rut_cliente      VARCHAR(12) NOT NULL,
    numero_serie     VARCHAR(9)  NOT NULL,
    nombre_cliente   VARCHAR(50) NOT NULL,
    apellido_cliente VARCHAR(50) NOT NULL,
    telefono_cliente VARCHAR(15) NOT NULL,
    email_cliente    VARCHAR(50),
    fecha_creacion   TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    estado           SMALLINT    NOT NULL,
    id_profesion     BIGINT,
    CONSTRAINT pk_cliente PRIMARY KEY (rut_cliente)
);

CREATE TABLE direccion_cliente
(
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    calle          VARCHAR(50)                             NOT NULL,
    numero         VARCHAR(5)                              NOT NULL,
    depta          VARCHAR(5)                              NOT NULL,
    tipo_direccion SMALLINT                                NOT NULL,
    ciudad         VARCHAR(50)                             NOT NULL,
    rut_cliente    VARCHAR(12),
    CONSTRAINT pk_direccioncliente PRIMARY KEY (id)
);

CREATE TABLE profesion
(
    id_profesion     BIGINT      NOT NULL,
    nombre_profesion VARCHAR(50) NOT NULL,
    CONSTRAINT pk_profesion PRIMARY KEY (id_profesion)
);

